package loop_study;

public class For_p1 {

	public static void main(String[] args)
	{
		System.out.println("Multiplication table of 4 ");
		
		for(int i=4;i<=40;i=i+4     )
		{
			 System.out.println(i);
		}
		
		
		System.out.println("Addition of 1 to 20 numbers");
		int sum=0;
		for(int i=1;i<=20;i++)
		
		  sum=sum+i;
		System.out.println("Addition is "+sum);
			
		

	}

}
